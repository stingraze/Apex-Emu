# Parallel Mandelbrot — each vCPU blits its row band from rodata into guest RAM.
# Host: RCX=y0, RDX=y_end, RDI=vCPU id.

    .equ WIDTH,  64
    .equ FB_BASE, 0x200000

    .text
    .globl _start
_start:
    push    %rbp
    mov     %rsp, %rbp
    push    %rbx
    push    %rdi                    # save vCPU id (rdi clobbered in row loop)

    mov     %rcx, %rbx              # y = y0

.Lrow:
    cmp     %rdx, %rbx
    jae     .Ldone

    # src = art + y*64
    mov     %rbx, %rax
    mov     $6, %rcx
.Lshl_src:
    test    %rcx, %rcx
    jz      .Lshl_src_done
    add     %rax, %rax
    sub     $1, %rcx
    jmp     .Lshl_src
.Lshl_src_done:
    lea     art(%rip), %rsi
    add     %rax, %rsi

    # dst = FB_BASE + y*64
    mov     %rbx, %rax
    mov     $6, %rcx
.Lshl_dst:
    test    %rcx, %rcx
    jz      .Lshl_dst_done
    add     %rax, %rax
    sub     $1, %rcx
    jmp     .Lshl_dst
.Lshl_dst_done:
    mov     $FB_BASE, %rdi
    add     %rax, %rdi

    xor     %rcx, %rcx
.Lcopy:
    cmp     $8, %rcx
    jae     .Lcopy_done
    mov     (%rsi), %rax
    mov     %rax, (%rdi)
    add     $8, %rsi
    add     $8, %rdi
    add     $1, %rcx
    jmp     .Lcopy
.Lcopy_done:

    add     $1, %rbx
    jmp     .Lrow

.Ldone:
    pop     %rdi                    # vCPU id
    xor     %rdi, %rdi              # exit(0) — id visible in debug via saved regs
    mov     $60, %rax
    syscall

    pop     %rbx
    pop     %rbp
    ret

    .p2align 4
